# ASR-Translator Monitoring

This directory contains the monitoring setup for the ASR-Translator microservice.

## Components

- **Prometheus**: Collects and stores metrics
- **Grafana**: Visualizes metrics with dashboards

## Usage

1. Install Docker and Docker Compose if not already installed:
   ```
   sudo apt-get update
   sudo apt-get install docker.io docker-compose
   ```

2. Start the monitoring stack:
   ```
   cd monitoring
   docker-compose up -d
   ```

3. Access the dashboards:
   - Prometheus: http://localhost:9090
   - Grafana: http://localhost:3000 (login with admin/admin)

## Configuration

- Prometheus is configured to scrape metrics from the ASR-Translator services.
- Pre-configured Grafana dashboard shows key metrics including processing times, memory usage, and queue sizes.

## Updating Configuration

If you need to update the configuration:

1. Edit the appropriate file in the monitoring directory
2. Restart the containers:
   ```
   docker-compose restart
   ```

## Stopping

To stop the monitoring stack:
```
docker-compose down
```
EOF

